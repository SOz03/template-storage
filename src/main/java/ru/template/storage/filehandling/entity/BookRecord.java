//    Должно быть реализовано ведение книги поступивших ходатайств дознавателей об обжаловании постановлений прокурора о возвращении уголовного дела для производства дополнительного дознания либо пересоставления обвинительного акта (постановления), а также решений вышестоящего прокурора, принятых по результатам их рассмотрения.
//    Книга поступивших ходатайств дознавателей об обжаловании постановлений прокурора о возвращении уголовного дела для производства дополнительного дознания либо пересоставления обвинительного акта (постановления), а также решений вышестоящего прокурора, принятых по результатам их рассмотрения должна содержать следующие атрибуты:
//             N п/п;
