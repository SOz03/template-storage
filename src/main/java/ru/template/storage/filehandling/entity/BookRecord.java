    /**
     * дата возбуждения
     */
    private LocalDate initiationDate;

    /**
     * Ф.И.О. подозреваемого
     */
    private String fullname;

    /**
     * статья УК РФ
     */
    private String criminalCode;


// Дата поступления прокурору уголовного дела с обвинительным постановлением;
